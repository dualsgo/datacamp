"""Funções familiares
O Python oferece uma série de funções integradas para facilitar a vida de um cientista de dados. Você já conhece duas dessas funções: print() e type(). Há também funções como str(), int(), bool() e float() para alternar entre os tipos de dados. Você pode saber mais sobre eles aqui. Essas também são funções integradas.

Chamar uma função é fácil. Para obter o tipo de 3.0 e armazenar a saída como uma nova variável, result, você pode usar o seguinte:

result = type(3.0)
Instruções

Use print() em combinação com type() para imprimir o tipo de var1.
Use len() para obter o comprimento da lista var1. Envolva-o em uma chamada print() para imprimi-lo diretamente.
Use int() para converter var2 em um número inteiro. Armazene a saída como out2."""

# Create variables var1 and var2
var1 = [1, 2, 3, 4]
var2 = True

# Print out type of var1
print(type(var1))

# Print out length of var1
print(len(var1))

# Convert var2 to an integer: out2
out2 = int(var2)